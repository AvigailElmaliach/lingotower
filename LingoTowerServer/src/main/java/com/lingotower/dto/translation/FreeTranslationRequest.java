package com.lingotower.dto.translation;

public class FreeTranslationRequest {
	private String text;
	private String sourceLang;
	private String targetLang;

	public String getText() {
		return text;
	}

	public String getSourceLang() {
		return sourceLang;
	}

	public String getTargetLang() {
		return targetLang;
	}

}
